package com.paimon.lumine;

public class subStringPractice {

    public static void main(String[] args) {
        String s = "I love cats";
        System.out.println(s.length());

            
        for (int i = 7; i < s.length(); i++) {
            System.out.print(s.charAt(i));
        }
    }


    

}
